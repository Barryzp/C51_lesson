#include "timer0.h"


void timer0_init(void) // 1000微秒@11.0592MHz
{
    // AUXR |= 0x80;		//定时器时钟1T模式
    TMOD &= 0xF0; // 设置定时器模式
    TL0 = 0xCD;   // 设置定时初值
    TH0 = 0xD4;   // 设置定时初值
    TF0 = 0;      // 清除TF0标志
    TR0 = 1;      // 定时器0开始计时

    // 中断设置
    ET0 = 1; // 使能定时器0中断
    EA = 1;  // 使能总中断

    // 配置优先级，目前只有一个中断源可以不写，默认最低级0，为了完整还是写上
    PT0 = 0; // 定时器0中断优先级低
}